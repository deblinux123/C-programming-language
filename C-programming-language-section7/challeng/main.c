#include <stdio.h>


int gcdAlgorithem(int a, int b)
{
    int r = 0;
    
    while(b != 0)
    {
        r = a % b;
        a = b;
        b = r; 
    }
    
    return a;
}


int main()
{
    int a = 0;
    int b = 0;
    
    printf("Please enter the 2 numbers: \n");
    scanf("%d %d", &a, &b);
    
    int result = gcdAlgorithem(a, b);
    printf("result is : %d\n", result);
    
    return 0;
}