#include <stdio.h>

/*
در زبان C، عملگرهای بیتی (Bitwise Operators) ابزارهایی هستن که به‌جای کار روی مقادیر عددی به‌صورت کامل، روی بیت‌های منفرد عدد عمل می‌کنن.
---

## 🧠 کاربرد اصلی:

موقعی به کار میان که بخوای با بیت‌های یک عدد (0 و 1) کار کنی — مثلاً برای:

* فشرده‌سازی داده‌ها
* تنظیم یا بررسی وضعیت‌ها (flags)
* برنامه‌نویسی سطح پایین (مثل درایورها یا میکروکنترلرها)

---

## ✅ انواع عملگرهای بیتی در C

| عملگر | نام         | توضیح                                   | مثال                      |     |     |
| ----- | ----------- | --------------------------------------- | ------------------------- | --- | --- |
| `&`   | AND         | بیت‌ها را با هم AND می‌کند              | `a & b`                   |     |     |
| \`    | \`          | OR                                      | بیت‌ها را با هم OR می‌کند | \`a | b\` |
| `^`   | XOR         | بیت‌ها را با هم XOR می‌کند (متفاوت = 1) | `a ^ b`                   |     |     |
| `~`   | NOT         | همه بیت‌ها را معکوس می‌کند (1→0, 0→1)   | `~a`                      |     |     |
| `<<`  | Left Shift  | بیت‌ها را به چپ می‌برد                  | `a << 2`                  |     |     |
| `>>`  | Right Shift | بیت‌ها را به راست می‌برد                | `a >> 2`                  |     |     |

---

---

## 🧠 نکات مهم:

* این عملگرها فقط روی **اعداد صحیح (int, char, etc.)** کار می‌کنن، نه `float` یا `double`.
* برای استفاده با `unsigned int` بهترن، چون عدد منفی ممکنه مشکلاتی در shift ایجاد کنه.
* خیلی در برنامه‌نویسی سخت‌افزار و تنظیم بیت‌ها به کار می‌رن.

---

*/

int main()
{
    int a = 5;                        // 00000101
    int b = 3;                        // 00000011
    
    printf("a & b = %d\n", a & b);    // 00000001 -> 1
    printf("a | b = %d\n", a | b);    // 00000111 -> 7
    printf("a ^ b = %d\n", a ^ b);    // 00000110 -> 6
    printf("~a = %d\n", ~a);          // 11111010 -> -6
    printf("~b = %d\n", ~b);          // 11111100 -> -4
    printf("a << 1 = %d\n", a << 1);  // 00001010 -> 10
    printf("a >> 1 = %d\n", a >> 1);  // 00000010 -> 2
    
    return 0;
    
}